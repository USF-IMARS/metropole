        <h1>Advancing International Adaptation Research While Supporting Community-Driven Planning</h1>
        <p>The international METROPOLE research project brought together social, economic, environmental scientists and local practitioners to assess the socio-economic and cultural factors which influence resiliency planning and adaptive capacity among local institutions in three study areas in Brazil, the UK and the US.</p>

        <p>Metropole is one of six international, multi-year Coastal Vulnerability projects funded by the Belmont Forum, a group of the world's major and emerging funders of global environmental change research. The Belmont Forum aims to accelerate delivery of the environmental research needed to remove critical barriers to sustainability by aligning and mobilizing international resources. It pursues the goals set in the <a href="https://belmontforum.org/belmont-challenge">Belmont Challenge</a> by adding value to existing national investments and supporting international partnerships in interdisciplinary and transdisciplinary scientific endeavors.</p>

        <p>The Metropole research produced new insights to enhance resiliency planning processes and public funding strategies that can support local infrastructure improvements and international adaption research methods. The project advances discovery and understanding of connections between values, beliefs, and decision-making processes regarding risks, adaptation options, costs, and public benefit. Our approach builds on the latest research on visualization tools, community response to hazards, risk assessments, and organizational barriers to adaptation.</p>

