      <h1>Integrated Research Assesses Adaptive Capacity and Stakeholder Values and Priorities</h1>
        <p>The METROPOLE project encompassed an integrated environmental, economic and social analyses which engaged key stakeholders and decision makers in two municipal planning workshops and in-person interviews. The objectives were to analyze:</p>
        <ul>
          <li>How attitudes and values of decision makers influence receptivity to scientific/economic data and scenarios, and build flexible governance approaches;</li>
          <li>How stakeholders perceive and respond to locally specific scientific knowledge and economic and adaptation options presented in visualizations;</li>
          <li>Decision making priorities for defined adaptation options, and perspectives on public finance;</li>
          <li>Regional adaptive capacity and institutional factors that support organizational ability to adapt and to mobilize toward change</li>
        </ul>
        <p>The assessment tools included:</p>
        <ol>
          <li><strong><a href="coast.php">Coastal Adaptation to Sea Level Rise Tool (COAST)</a></strong> model, developed by Sam Merrill et al, to show visualizations to stakeholders and discus SLR, infrastructure impacts, costs/benefits for adaptations, and define their perceptions of community estimates for action.</li>
          <li><strong><a href="survey.php">Values/priorities surveys</a></strong> deployed pre and post workshops to identify links between participant beliefs, values, attitudes about local government action for possible adaptation action and public finance, and to assess changes after seeing the COAST visualizations and discussing scenarios.</li>
          <li><strong><a href="aci.php">Adaptive Capacity Index (ACI)</a></strong> an assessment of institutional and individual interactions that shape local and regional adaptive capacity, developed by Mark Pelling et al. The ACI has been applied in multiple nations to evaluate flood risks, among other resiliency issues.</li>
        </ol>
        <p>By triangulating these elements, we evaluated stakeholder values and priorities, and organizational dynamics and institutional factors which shape local municipal planning processes for resiliency.</p>


