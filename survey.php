    <h2 id="survey">Metropole Surveys: Assessing Values, Local Government Priorities and Acceptability of Public Finance Mechanisms</h2>
    <p>Metropole researchers gave surveys to workshop participants just prior to Workshop 1and at the conclusion of Workshop 2. New people who came to Workshop 2 were also given a “pre-workshop” survey.</p>

    <p>The survey questions investigated: 1) links between attitudes, values, priorities for adaptation planning and perspectives for how to pay for such actions, and 2) whether the visualizations and workshop process had effects on such priorities and acceptability. Survey questions examined hazard exposure, perceptions about 16-prototypical adaptation options a local government could implement and acceptability of public finance methods used by local government. Demographics included race, age, education, gender and political affiliation.</p>

    <p>To ascertain environmental and governance values that may shape adaptation perceptions and decision making, the survey included an abbreviated version of the New Ecological Paradigm (NEP) value scale (Dunlap and Van Liere 1978; Dunlap et al. 2000). The NEP has been used for almost four decades in many countries. We also sought to assess normative beliefs about barriers by asking participants to identify reasons of why others in their community might not support local adaptation action.</p>

    <p>Several papers reviewed different aspects of the findings have been submitted and are in development.</p>

